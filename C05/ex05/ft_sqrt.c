#include <stdio.h>

int	ft_sqrt(int nb)
{
	int	n;

	n = 0;
	while (nb > 0)
	{
		nb -= (n + 1);
		n++;
		n++;
	}
	if (nb < 0)
		return (0);
	return (n / 2);
}

