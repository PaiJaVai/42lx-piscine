#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	soma(char a, char b)
{
	ft_putchar('0' + a / 10);
	ft_putchar('0' + a % 10);
	ft_putchar(' ');
	ft_putchar('0' + b / 10);
	ft_putchar('0' + b % 10);
	if ((a < 98) || (b < 99))
	{
		ft_putchar(',');
		ft_putchar(' ');
	}
}

void	ft_print_comb2(void)
{
	char	a;
	char	b;

	a = 0;
	while (a <= 98)
	{
		b = a + 1;
		while (b <= 99)
		{
			soma(a, b);
			b++;
		}
		a++;
	}
}
